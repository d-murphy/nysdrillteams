// data source:  https://data.ny.gov/Public-Safety/Fire-Department-Directory-for-New-York-State/qfsu-zcpv/data_preview


(async function(){
    const data = loadData();
    const townNames = data.map(name => {
        const segs = name.split(" ");
        const newName = segs.filter(el => {
            return !["FIRE", "DEPT", "DEPARTMENT", "NO", "HOOK", "LADDER", "AND", "&", "(3-G)", 
                "1", "VOLUNTEER", "CO", "(CITY)", "COMPANY", "DISTRICT", "#2", "#3", "FD", "VFD", "DEPT(NO2)", 
                "HOSE", "VOL", "VILLAGE", "DIST", "#5", "(TOWN)", "#1"
            ].includes(el.toUpperCase())
        })
        .map(el => el.slice(0, 1).toUpperCase() + el.slice(1).toLowerCase())
        .join(" ").trim();
        return {
            name: newName,
            type: "town"
        }
    })
    // const townNamesString = townNames.map(name => name.name).join("\n");
    // console.log(townNamesString);

    const writeResult = await writeDocs('simulation-fantasy-name-suggestions', townNames); 
    console.log('write result: ', writeResult); 


})()


async function getCollection(collectionName){
    const dbConnectionStr =
        `mongodb+srv://${dbUn}:${dbPass}@nysdrillteams.4t9radi.mongodb.net/?retryWrites=true&w=majority`;
    const dbPromise = await getDbPromise(dbConnectionStr, DB_NAME);
    const collection = await getCollectionPromise(dbPromise, collectionName); 
    return collection; 
}

async function writeDocs(collectionName, documents){
    let tempCollection = await getCollection(`temp-${collectionName}`); 
    let result, renameResult; 
    try {
        console.log('starting write to db: '); 
        result = await tempCollection.insertMany(documents)
        console.log('write to db finished.  Starting rename.');
        renameResult = await tempCollection.rename(collectionName, {dropTarget: true})
        console.log('rename complete.');  
    } catch(e) {
        console.log('error during db write: ', e); 
    }
    return renameResult; 
}



